print ("Metodo UPPER\n")
print ("el metodo upper transforma todo el texto en mayusculas")                         #imprimir una cadena de texto
cadena = "el metodo upper transforma todo el texto en mayusculas"                        #se asigna la cadena a una variable
print(cadena.upper())                                                                    #se imprime la variable cadena y se le aplica el metodo upper

print ("\nMetodo LOWER\n")
print ("EL METODO LOWER TRANSFORMA EL TEXTO EN MINUSCULAS")                              #imprimir una cadena de texto
cadena1 = "EL METODO LOWER TRANSFORMA EL TEXTO EN MINUSCULAS"                            #se asigna la cadena a una variable
print(cadena1.lower())                                                                   #se imprime la variable cadena1 y se le aplica el metodo lower

print ("\nMetodo TITLE\n")                                                               
print ("EL METODO TITLE TRANSFORMA A MAYUSCULAS LA PRIMERA LETRA DE CADA PALABRA")       #imprimir una cadena de texto
cadena2 = "EL METODO TITLE TRANSFORMA A MAYUSCULAS LA PRIMERA LETRA DE CADA PALABRA"     #se asigna la cadena a una variable    
print(cadena2.title())                                                                   #se imprime la variable cadena2 y se le aplica el metodo title

